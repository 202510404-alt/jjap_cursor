# INFO: 현재는 file_scanner/ 서브패키지 + scan_project / write_scan_outputs 함수로 전환됨.


class FileScanner:
    """Thin facade delegating to scan_project and write_scan_outputs (하위 호환용)."""

    def __init__(self, project_root: Path) -> None:
        self.project_root = project_root

    def scan_project(self) -> ScanArtifacts:
        return scan_project(self.project_root)

    def write_outputs(self, artifacts: ScanArtifacts) -> None:
        write_scan_outputs(self.project_root, artifacts)


__all__ = [
    "ScanArtifacts",
    "FileScanner",
    "scan_project",
    "write_scan_outputs",
]
