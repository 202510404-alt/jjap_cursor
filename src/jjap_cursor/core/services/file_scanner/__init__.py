# INFO: class FileScanner:
# INFO:     def __init__(self, project_root: Path) -> None: ...
# INFO:     def scan_project(self) -> ScanArtifacts: ...
# INFO:     def write_outputs(self, artifacts: ScanArtifacts) -> None: ...
# INFO:     def _iter_python_files(self) -> list[Path]: ...
# INFO:     def _is_ignored(self, path: Path) -> bool: ...
# INFO:     def _load_gitignore_patterns(self) -> list[str]: ...
# INFO:     def _matches_directory_ignore(self, rel_root: str, dirname: str) -> bool: ...
# INFO:     def _extract_skeleton(self, module: ast.Module) -> str: ...
# INFO:     def _render_class_skeleton(self, node: ast.ClassDef) -> list[str]: ...
# INFO:     def _render_function_skeleton(self, node, indent: int) -> list[str]: ...
